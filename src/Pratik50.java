public class Pratik50 {

    public static void main(String[] args) {
        //Ic ice for dongusu kullanarak bir carpim tablosu hazirlayin

        for (int i = 1; i <=10 ; i++) {
            for (int j = 1; j <=10 ; j++) {
                System.out.println(i + "*" + j + "=" + i*j);
            }
            System.out.println();
        }



    }

    public static class C01_Pratik {
        public static void main(String[] args) {
            /*
            Bir array icersindeki verileri for fongusu kullanarak
            konsola yazdiran bir program yaziniz.
             */
            int [] arr = {20,50,70,80};
            for (int i = 0; i <arr.length ; i++) {
                System.out.println(arr[i]);
            }



        }
    }
}
